package main

import "fmt"

func main() {
	// Printing a decimal number
	fmt.Println(42)

	// Printing a decimal number and it's binary representation
	fmt.Printf("%d - %b \n", 42, 42)

	// Printing a decimal number and it's octal representation
	fmt.Printf("%d - %o \n", 42, 42)

	// Printing a decimal number and it's hexadecimal representation
	fmt.Printf("%d - %X \n", 42, 42)
}
